      <h2>SWEET POTATOES</h2>
      <h5>Sweet Potato farming,Sept 01, 2018</h5>
      <div class="fakeimg" style="height:200px;"><img src="farmer/product_images/sp4.jpg"></div>
    <p>Sweet potatoes are typically grown from slips, which are sprouts that are grown from stored sweet potatoes. You can buy slips from garden centers, nurseries, or local farmers.
You can also create your own slips to plant in the spring. In November (this is when the best of the new harvest will be out), go to your supermarket and look for unblemished and uncracked medium-size sweet potatoes. One sweet potato should yield about 12 plants.
Store sweet potato slips in a well-lit room with a temperature between 65° and 70°F. Keep them there until about 90 days before the last spring frost date. They will then need to be embedded in soil for 90 days and kept continuously warm and moist.
Use a 1-½ gallon pot for every two slips. Remember to poke drainage holes in the bottom of the pot and fill it with 3 inches of mulch, followed by garden or potting soil. Plant the slips in the pot at a 45° angle so that the sprouts will grow above the soil. When the slips are 6 to 12 inches tall, you can plant them outdoors, as long as all danger of frost has passed.
After you have grown your own slips or bought them, till the area of the garden you will be using to a depth of 8 to 10 inches. Create raised mounds 6 to 8 inches tall and about 12 inches wide. Use fertile, well-drained soil.
Plant the slips 12 to 18 inches apart in the bed, after the last spring frost date. Plant the slips deep enough to cover the roots and about ½ inch of the stem. Water the slips with a starter solution (a liquid fertilizer) that is high in phosphorous, then water generously for a few days to make sure that the plants root well.
See more tips for growing sweet potatoes.</p>
