      <h2>TOMATOES</h2>
      <h5>Tomato Farming, Sep 2, 2017</h5>
      <div class="fakeimg" style="height:200px; width:300px;"><img src="farmer/product_images/tomaa.jpg"></div>
      <p>
        1.Sunlight.Tomatoes require a minimum of 8 hours of continuous sunlight each day.
2.Temperature.Tomatoes need 3 to 4 months of warm, clear, fairly dry weather to produce best. Tomatoes need consistent night temperatures between 55°F and 75°F to set fruit.  
3.Continuous and even watering.Keep the soil evenly moist–not too wet and not too dry.Too much water will drown the plant;too little water will stop fruit production. 
4.Continuous feeding.Prepare the tomato-growing bed with well-rotted garden compost and a trowelful of aged manure added to the soil where each plant will grow. 
5.Loose, well-drained soil.But almost any garden soil will grow tomatoes.Sandy loam is best for early tomatoes.Heavy clay loam is ideal for late tomatoes.Amend the soil with aged compost before planting.Tomatoes prefer a soil with a pH between 6.0 and 7.0, just below neutral in acidity.

6.Protect leaves and roots. Protect the tomato plant from extremes of temperature–cold and hot, strong winds, weeds, pests and diseases.Set 8 to 10 week old tomato seedlings in the garden two weeks after the last frost in spring.If transplanted sooner,tomatoes must be protected from cold temperatures with hot caps or plastic tunnels. </p>
